    /**
     * Debug some vars, array, int, string ...
     * @param object $var
     */
    public static function debug($var)
    {
        echo '<div style="padding: 10px; background: #1e1e1e; color: #fff; border-radius: 5px; margin: 10px 0;">';
        $debug = debug_backtrace();
        echo '
                <p>
                    <a href="" onclick="$(this).parent().next(\'ol\').slideToggle(); return false;">
                        <strong>' . $debug[0]['file'] . '</strong> (l. ' . $debug[0]['line'] . ')
                    </a>
                </p>
                <ol style="display: none;">
            ';
        foreach ($debug as $k => $v) {
            echo '
                    <li>
                        <strong>' . $v['file'] . '</strong> (l. ' . $v['line'] . ')
                    </li>
            ';
        }
        echo '</ol>';
        echo '<pre>';
        print_r($var);
        echo '</pre>';
        echo '</div>';
    }

